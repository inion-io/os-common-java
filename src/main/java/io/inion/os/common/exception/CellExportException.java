package io.inion.os.common.exception;

/**
 * @author Marcus Bock
 */
public class CellExportException extends CellException {

	public CellExportException() {
		super();
	}

	public CellExportException(String message) {
		super(message);
	}

	public CellExportException(String message, Throwable cause) {
		super(message, cause);
	}

	public CellExportException(Throwable cause) {
		super(cause);
	}

}