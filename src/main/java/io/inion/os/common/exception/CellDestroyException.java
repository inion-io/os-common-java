package io.inion.os.common.exception;

/**
 * @author Marcus Bock
 */
public class CellDestroyException extends CellException {

	public CellDestroyException() {
		super();
	}

	public CellDestroyException(String message) {
		super(message);
	}

	public CellDestroyException(String message, Throwable cause) {
		super(message, cause);
	}

	public CellDestroyException(Throwable cause) {
		super(cause);
	}

}